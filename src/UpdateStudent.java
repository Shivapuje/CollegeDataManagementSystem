    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String std;
        std=jTextField4.getText();
        DatabaseConnect db=new DatabaseConnect(database);

        db.close();
        String quer;
        quer= "SELECT * FROM std_prof WHERE reg_no='"+std+"'";
        
         try {
            rs = db.exeQuerry(quer);
            if(rs.next())
            {
                
                
                jTextField1.setText(rs.getString("fn"));
                jTextField2.setText(rs.getString("mn"));
                jTextField3.setText(rs.getString("ln"));
                jTextField5.setText(rs.getString("age"));
                jComboBox1.setSelectedItem(rs.getString("sem"));
                jTextField11.setText(rs.getString("mob"));
                jTextField7.setText(rs.getString("roll_no"));
                jTextField13.setText(rs.getString("email"));
                jTextField12.setText(rs.getString("home"));
                jTextArea1.setText(rs.getString("add1"));
                jTextArea2.setText(rs.getString("add2"));
              jTextField4.setEnabled(false);
          
         }
            else
            {
                JOptionPane.showMessageDialog(null,"Please Enter Valid Register Number");
            }
         }
         catch (Exception ex) {
            Logger.getLogger(UpdateStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


