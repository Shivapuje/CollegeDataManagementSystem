    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      DatabaseConnect db=new DatabaseConnect(database);
      String sem,sub,quer;
      sem=jComboBox1.getSelectedItem().toString();
      sub=jTextField1.getText();
      quer="INSERT INTO sub_sem("+sem+") VALUES('"+sub+"')";

      if(sub.length()!=0)
      {
          try {
              int b = db.exe(quer);
              if(b!=0)
              {
                  JOptionPane.showMessageDialog(null,"Subject Added");
                  
              }
          } catch (Exception ex) {
              Logger.getLogger(AddSubject.class.getName()).log(Level.SEVERE, null, ex);
          }
          
      }
      else
      {
       JOptionPane.showMessageDialog(null,"Please Enter the subject name");   
      }
    }//GEN-LAST:event_jButton1ActionPerformed

